# P(6) = 3 = 1 + 2 = 1(=i-5) + (i-1)
# P(7) = 4 = 1 + 3 = 1(=i-5) + (i-1)
# P(8) = 5 = 1 + 4 = 1(=i-5) + (i-1)
# P(9) = 7 = 5 + 2 = 2(=i-5) + (i-1)
# P(10) = 9 = 7 + 2 = 2 + (i-1)
# P(11) = 12 = 9 + 3 = 3 + (i-1)
t = int(input())

dp = [0] * 101

dp[1], dp[2], dp[3] = 1, 1, 1
dp[4], dp[5] = 2, 2

for i in range(6, 101):
    dp[i] = dp[i-5] + dp[i-1]

for _ in range(t):
    case = int(input())
    print(dp[case])

